<script>
import { isFinite } from 'lodash';

let d3 = null;

export default {
  name: 'ProgressDonut',
  props: {
    data: { type: Number, default: 0.5 },
  },
  mounted() {
    d3 = window.d3; // eslint-disable-line
    this.handleData(this.data);
  },
  computed: {
    el() {
      return this.$el;
    },
  },
  watch: {
    data(val) {
      this.handleData(val);
    },
  },
  methods: {
    // 处理数据
    handleData(val) {
      if (isFinite(val)) {
        const data = (1 - val) * 136;
        const svg = d3.select(this.el);
        const group = svg.select('g');
        group
          .selectAll('path')
          .data(new Array(136))
          .attr('fill', (d, i) => {
            if (i < data) {
              return '#dcecf7';
            }
            return '#3890ff';
          });
      }
    },
  },
};
</script>
